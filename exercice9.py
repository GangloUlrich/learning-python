### Écrire un programme Python qui permet de calculer la moyenne de trois entiers saisis par l'utilisateur.

nbr_1 = float(input('Entrer le premier nombre \n'))
nbr_2 = float(input('Entrer le deuxieme nombre \n'))
nbr_3 = float(input('Entrer le troisieme nombre \n'))

moyenne = (nbr_3 + nbr_2 + nbr_1) / 3;

print(f'La moyenne des nombres {nbr_1} + {nbr_2} + {nbr_3} est égale à {moyenne}');

